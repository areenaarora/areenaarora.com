	<h2>Just a bit more...</h2>
	<p>
		I’m a Data and Investigative Reporter and was most recetly covering education for Knox News, a
		Gannett-owned local newspaper in Knoxville, Tennessee.
	</p>
	<p>
		Americans care about public education in a way I hadn't fully comprehended two and a half years
		ago. They also politicize education in a way I couldn't have anticipated. Covering public
		schools has been truly rewarding, and hopefully, someday (soon), I'll write a book about how
		America educates its kids and what I’ve learned, liked and wrestled with.
	</p>
	<p>
		Lately, I’ve also been diving into the evolution of the news and information industry, and the
		intersection of generative AI, ethics, and journalism. I love exploring tools that make
		reporting easier.
	</p>
	<p>
		I have a master’s degree in Data Journalism from Columbia University where I took classes in
		environmental reporting, algorithms and mapping, among other courses. I’ve also worked as a city
		beat reporter for a local newsroom in Idaho, worked for Reuters as a publisher and as an editor
		at a media start-up.
	</p>
	<p>
		At Knox News, as the first and only data reporter in the newsroom, I built datasets to have
		ready for breaking news, including databases for property ownership and crime stats.
	</p>
	<p>
		A lot of Knox County and Tennessee’s data is made available in formats that are not machine
		readable, and I’m constantly finding ways to navigate that, both by making connections with data
		officers and through coding solutions.
	</p>
	<p>
		For visualization, I’m trained in making interactives with Python libraires such as Altair and
		Plotly and D3. I enjoy mapping with QGIS, Mapbox and Google Earth Pro. For quick turnarounds, I
		also rely on free resources such as Datawrapper and Flourish.
	</p>
	<p>I once wrote a weekly newsletter about life in NYC and grad school.</p>
	<p>
		I take great pride in uncovering hidden stories with a fresh perspective in unfamiliar
		situations!
	</p>
