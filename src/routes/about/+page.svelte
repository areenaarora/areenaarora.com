	<h2>Just a bit more...</h2>
	<p>
		I’m a Data and Investigative Reporter at Knox News, a Gannett-owned local newspaper in
		Knoxville, Tennessee. I have a master’s degree in Data Journalism from Columbia University where
		I took classes in environmental reporting, algorithms and mapping, among other courses. I’ve
		also worked as a city beat reporter for a local newsroom in Idaho, worked for Reuters as a
		publisher and at a media start-up.
	</p>
	<p>
		In my current role as the first and only data reporter in the newsroom, I’ve worked on building
		solutions to have datasets ready in case of breaking news, including databases for property
		ownership and crime stats.
	</p>
	<p>
		For midterm elections, I built results' data maps that had never been done in this newsroom
		before. A lot of Knox County and Tennessee’s data is made available in formats that are not
		machine readable, and I’m constantly finding ways to navigate that, both by making connections
		with data officers and through coding solutions.
	</p>
	<p>
		For visualization, I’m trained in making interactives with Python libraires such as Altair and
		Plotly and I’m constantly working to improve my D3 skills. I enjoy mapping with QGIS, Mapbox and
		Google Earth Pro. For quick turnarounds, I also rely on free resources such as Datawrapper and
		Flourish.
	</p>
	<p>
		I once wrote a weekly newsletter about life in NYC and grad school — but, I'm unsure now of the
		direction I want to take it to.
	</p>
	<p>
		I take great pride in uncovering hidden stories with a fresh perspective in unfamiliar
		situations!
	</p>
